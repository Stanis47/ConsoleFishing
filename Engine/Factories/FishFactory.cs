        public static List<Fish> CreateFishByID(params int[] speciesID)
        {
            List<Fish> result = new List<Fish>();

            foreach (int ID in speciesID)
            {
                Fish fish = _allFish.FirstOrDefault(fish => fish.SpeciesID == ID);

                if (fish != null)
                {
                    result.Add(fish.Clone());
                }
            }

            return result;
        }
